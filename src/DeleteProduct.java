    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:

        String SerialNO =Product.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from producttable where SerialNO='"+SerialNO+"'");
            if(rs.next())
            {
                partNo.setText(rs.getString(2));
                Name.setText(rs.getString(3));
                Rate.setText(rs.getString(4));
                Quantity.setText(rs.getString(5));
                Description.setText(rs.getString(6));
                Activate.setText(rs.getString(7));
                Product.setEditable(false);
            }
            else
            JOptionPane.showMessageDialog(null,"Serial No. does not Exit");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SearchActionPerformed

