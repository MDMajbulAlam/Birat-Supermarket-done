    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ID=Id.getText();
        String AdminName=Name.getText();
        String contactNo=Contact.getText();
        String email=Email.getText();
        String address=Address.getText();
        String AdminPass=Password.getText();
        String gender=(String)gendercb.getSelectedItem();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("insert into admintables values('"+ID+"','"+AdminName+"','"+contactNo+"','"+email+"','"+address+"','"+AdminPass+"','"+gender+"')");
            JOptionPane.showMessageDialog(null,"Successfully Admin Updated");
            setVisible(false);
            new Admin().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Mobile Number is already Exit");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

